#!/usr/bin/env node
/**
 * Rasterizes Natural Earth 110m land polygons into a bit-packed bitmap
 * at 0.5° resolution (720 × 360 = 259 200 bits ≈ 32 KB).
 *
 * Output: src/lib/land-mask-data.ts — exports a Uint8Array and the grid spec.
 * Run once via:  node scripts/build-land-mask.mjs
 */
import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const geojsonPath = process.argv[2] || '/tmp/ne_110m_land.geojson';
const outPath = path.join(__dirname, '..', 'src', 'lib', 'land-mask-data.ts');

const RES = 0.5;        // degrees per cell
const W = Math.round(360 / RES); // 720
const H = Math.round(180 / RES); // 360

// Ray casting point-in-polygon (classic algorithm).
function pointInRing(lon, lat, ring) {
  let inside = false;
  for (let i = 0, j = ring.length - 1; i < ring.length; j = i++) {
    const xi = ring[i][0], yi = ring[i][1];
    const xj = ring[j][0], yj = ring[j][1];
    const intersect = ((yi > lat) !== (yj > lat))
      && (lon < (xj - xi) * (lat - yi) / (yj - yi) + xi);
    if (intersect) inside = !inside;
  }
  return inside;
}

function pointInPolygon(lon, lat, polygon) {
  // polygon: [ [outer ring], [hole1], [hole2], ... ]
  if (!pointInRing(lon, lat, polygon[0])) return false;
  for (let i = 1; i < polygon.length; i++) {
    if (pointInRing(lon, lat, polygon[i])) return false; // in a hole
  }
  return true;
}

// Bbox test to skip polygons far from the point.
function ringBbox(ring) {
  let minX = Infinity, maxX = -Infinity, minY = Infinity, maxY = -Infinity;
  for (const [x, y] of ring) {
    if (x < minX) minX = x;
    if (x > maxX) maxX = x;
    if (y < minY) minY = y;
    if (y > maxY) maxY = y;
  }
  return [minX, minY, maxX, maxY];
}

const gj = JSON.parse(fs.readFileSync(geojsonPath, 'utf8'));
const polys = []; // { bbox, polygon }
for (const feat of gj.features) {
  const g = feat.geometry;
  if (!g) continue;
  if (g.type === 'Polygon') {
    polys.push({ bbox: ringBbox(g.coordinates[0]), polygon: g.coordinates });
  } else if (g.type === 'MultiPolygon') {
    for (const poly of g.coordinates) {
      polys.push({ bbox: ringBbox(poly[0]), polygon: poly });
    }
  }
}
console.log(`Loaded ${polys.length} polygons.`);

// Rasterize. Sample at cell center.
const bits = new Uint8Array(Math.ceil(W * H / 8));
let land = 0;
for (let y = 0; y < H; y++) {
  // lat goes from +90 (top, y=0) down to -90 (bottom, y=H).
  const lat = 90 - (y + 0.5) * RES;
  for (let x = 0; x < W; x++) {
    // lon goes from -180 (left) to +180 (right).
    const lon = -180 + (x + 0.5) * RES;
    let isLand = false;
    for (const p of polys) {
      const [minX, minY, maxX, maxY] = p.bbox;
      if (lon < minX || lon > maxX || lat < minY || lat > maxY) continue;
      if (pointInPolygon(lon, lat, p.polygon)) { isLand = true; break; }
    }
    if (isLand) {
      const idx = y * W + x;
      bits[idx >> 3] |= 1 << (idx & 7);
      land++;
    }
  }
  if (y % 30 === 0) process.stdout.write(`.`);
}
console.log(`\nLand cells: ${land} / ${W * H} (${((land / (W * H)) * 100).toFixed(1)}%).`);

// Encode as Base64.
const b64 = Buffer.from(bits).toString('base64');

const ts = `// AUTO-GENERATED by scripts/build-land-mask.mjs — do not edit by hand.
// Source: Natural Earth 110m land polygons (public domain).
// Resolution: ${RES}° per cell, ${W} × ${H} cells, bit-packed (LSB first).
/* eslint-disable */
export const LAND_MASK_RES = ${RES};
export const LAND_MASK_W = ${W};
export const LAND_MASK_H = ${H};
export const LAND_MASK_B64 = '${b64}';
`;
fs.writeFileSync(outPath, ts);
console.log(`Wrote ${outPath} (${bits.length} bytes raw, ${b64.length} chars base64).`);
